import { chromium } from 'playwright';
const browser = await chromium.launch();
const ctx = await browser.newContext({ viewport: { width: 1440, height: 900 } });
const p1 = await ctx.newPage();
await p1.goto('https://cardiff.co/newsroom/', { waitUntil: 'domcontentloaded', timeout: 60000 });
await p1.waitForTimeout(3000);
await p1.screenshot({ path: '/tmp/cardiff-newsroom-orig.png', fullPage: true });
const p2 = await ctx.newPage();
await p2.goto('http://localhost:3000/sites/cardiff-main.simplerdevelopment.com/newsroom', { waitUntil: 'domcontentloaded', timeout: 60000 });
await p2.waitForTimeout(3000);
await p2.screenshot({ path: '/tmp/cardiff-newsroom-port.png', fullPage: true });
await browser.close();
console.log('done');
